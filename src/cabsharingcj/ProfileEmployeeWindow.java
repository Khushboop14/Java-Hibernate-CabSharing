        Criteria crit = session.createCriteria(EmployeeSignin.class);
        List<EmployeeSignin> data = crit.list();
        if(data.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter data" );
        }
        else{

            EmployeeSignin empsn = (EmployeeSignin)session.get(EmployeeSignin.class, eid);
            empsn.setEmployeeid(eid);
            empsn.setName(name);
            empsn.setDesignation(desg);
            empsn.setAddress(adrs);

            session.update(empsn);
            tx.commit();
            JOptionPane.showMessageDialog(this, "Update details !!!" );

        }
