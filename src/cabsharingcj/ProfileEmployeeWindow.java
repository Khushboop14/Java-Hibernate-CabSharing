    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String uname = jTextField1.getText();
        String oldpass = new String (jPasswordField1.getPassword());
        String newpass = new String (jPasswordField2.getPassword());
        String confirmpass = new String(jPasswordField3.getPassword());

        SessionFactory sf = new Configuration().configure().buildSessionFactory();
        Session session = sf.openSession();
        Transaction tx = session.beginTransaction();

        Criteria crit = session.createCriteria(EmployeeSignin.class);
        crit.add(Restrictions.eq("password",oldpass));
        List<EmployeeSignin> data = crit.list();
        if(data.isEmpty()){
            JOptionPane.showMessageDialog(this, "invalid id/old password" );
        }
        else{
            if(newpass.equals(confirmpass)){
                EmployeeSignin empsn = (EmployeeSignin)session.get(EmployeeSignin.class, uname);
                empsn.setPassword(newpass);
                session.update(empsn);
                tx.commit();
                JOptionPane.showMessageDialog(this, "password has been changed" );
            }
            else{
                JOptionPane.showMessageDialog(this, "new password and confirm password does not  match" );
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

